package behavorial.command;

/**
 * The interface Electronic device.
 */
public interface ElectronicDevice {

    /**
     * On.
     */
    void on();

    /**
     * Off.
     */
    void off();

    /**
     * Volume up.
     */
    void volumeUp();

    /**
     * Volumen down.
     */
    void volumenDown();
}